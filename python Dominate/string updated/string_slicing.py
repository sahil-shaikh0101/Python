#string slicing

name="python is programming language"

# Print a given string.
print(name)

# Find the length of a string.
print(len(name))

# Print the first and last character of a string.

print(name[0])#first character
print(name[-1])#last character

# Print each character of a string using indexing.

print(name[0::])

# Reverse a string.

print(name[::-1])

# Extract the first 5 characters from a string using slicing.

print(name[0:5])

# Extract the last 4 characters from a string using slicing.

print(name[-4:])

# Print characters from index 2 to index 6.

print(name[2:7])

# Print every second character of a string.

print(name[0::2])

